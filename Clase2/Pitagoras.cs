using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Clase2
{
    public class Pitagoras
    {

        public static double CalcularPitagoras(int baset, int altura) {
            
            return Math.Sqrt(Math.Pow(baset,2) + Math.Pow(altura,2));

        
        }
    }
